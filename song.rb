class Song

  def initialize(name, artist, song_line)

    @name = name
    @artist = artist
    @song_line = song_line

  end #End of initialize function

  def name
      return @name
  end #End of name function

  def artist
      return @artist
  end #End of artist function

  def song_line
      return @song_line
  end ##End of song_line function

end #End of Song class
